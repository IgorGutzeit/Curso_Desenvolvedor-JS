//operadores matematicos

//soma
let soma = 5 + 2;
console.log(soma); // 7


//subtracao
let subtracao = 5 - 2;
console.log(subtracao); // 3


//Multiplicação
let multiplicacao = 5 * 2;
console.log(multiplicacao); // 10


//Divisão
let divisao = 5 / 2;
console.log(divisao); // 2.5


//Resto da divisão
let resto = 5 % 2;
console.log(resto); // 1


//Exponenciação
let exponenciacao = 5 ** 2;
console.log(exponenciacao); // 25


//operadores de incremento e decremento
let numero = 5;

//incremento
numero = numero + 1;
numero += 3;
numero++; // Igual a: numero = numero + 1

//decremento
numero = numero - 1;
numero -= 2;
numero--; // Igual a: numero = numero - 1


//Operador de concatenação de Strings
let nome = "João";
let sobrenome = "Souza";
let nomeCompleto = nome + sobrenome;

console.log(nomeCompleto);





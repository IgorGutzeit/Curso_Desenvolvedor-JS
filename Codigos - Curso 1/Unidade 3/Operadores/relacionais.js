// Operadores relacionais

//Igual a (==)
let igual = 5 == 5;
console.log(igual); // True


//Identico a (===)
let identico = 5 === "5";
console.log(identico); // False - Além de comparar o valor o operador também compara os tipos das variáveis

// Não igual (!=)
let naoIgual = 5 != 5;
console.log(naoIgual); // False


// Não Identico (!==)
let naoIdentico = 5 !== "5";
console.log(naoIdentico); // True


// Maior que
let maior = 5 > 3;
console.log(maior); // True


// Menor que
let menor = 5 < 3;
console.log(menor); // False


// Maior ou igual
let maiorIgual = 5 >= 5;
console.log(maiorIgual); // True


// Menor ou igual
let menorIgual = 5 <= 3;
console.log(menorIgual); // False